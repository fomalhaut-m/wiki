import sys

print('获取命令行参数')
print(sys.argv) # 获取命令行参数
print('# 获取python安装路径')
print(sys.path) # 获取python安装路径
print('# 获取python版本')
print(sys.version) # 获取python版本
print('# 获取python版本信息')
print(sys.version_info) # 获取python版本信息
print('# 获取python平台信息')
print(sys.platform) # 获取python平台信息
print('# 获取python安装路径')
print(sys.prefix) # 获取python安装路径
print('# 获取python执行文件路径')
print(sys.executable) # 获取python执行文件路径
print('# 获取python默认编码')
print(sys.getdefaultencoding()) # 获取python默认编码
print('# 获取python文件系统编码')
print(sys.getfilesystemencoding()) # 获取python文件系统编码
print('# 获取python运行环境信息')
print(sys.getwindowsversion()) # 获取python运行环境信息